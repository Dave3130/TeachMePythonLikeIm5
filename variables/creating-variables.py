# Tutorial: using variables in python
# ------------------------------------------------------------------------------------

# there's no need to declare variables in python. simply use them

# integers
a = 1
b = 2
print(a + b)

# strings
c = 'hello'
d = 'world'
print(c + d)

# bonus: you can check the type of a variable using the type() function
print(type(a))
print(type(c))

